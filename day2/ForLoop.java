package week1.day2;

public class ForLoop {

	public static void main(String[] args) {
		
		System.out.println("Increment number");
		for (int i = 0; i < 5; i++) {
			
			System.out.println(i);
		}

	
	System.out.println("reverse number");
	for(int j = 10;j>0;j--) {
		
		System.out.println(j);
		
	}

}
}
