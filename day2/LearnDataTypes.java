package week1.day2;

public class LearnDataTypes {
	
	public static void main(String[] args) {
		//datatype variable name = value;
		
		int age = 35;
		char initial = 'S';
		//String is non primitive datatype. It cannot be in lowercase 	
		String name = "Anu";
		boolean breakfast = true;
		float height = 16.5f;
		
		double accntbalance = 57.546;
		
		long phonenumber = 9344865412l;
		short rollNo = 12;
		
		System.out.println("My roll number is - " + rollNo);
		System.out.println("My initial is - " + initial +"."+ name);
		
		System.out.println("My age is - " + age);
		System.out.println("My height is - " + height);
		System.out.println("Had breakfast ? " + breakfast);
		System.out.println("My accntbalance is - " + accntbalance);
		System.out.println("My phonenumber is - " + phonenumber);
				
		
		
		
		
	}

}
